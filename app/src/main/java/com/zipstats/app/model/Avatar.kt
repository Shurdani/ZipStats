    fun getByCategory(category: AvatarCategory): List<Avatar> {
        return list.filter { it.category == category }
    }
    
    fun getPopular(): List<Avatar> {
        return list.filter { it.isPopular }
    }
    
    fun search(query: String): List<Avatar> {
        if (query.isEmpty()) return emptyList()
        val lowercaseQuery = query.lowercase()
        return list.filter { avatar ->
            avatar.emoji.contains(query) ||
            avatar.name.lowercase().contains(lowercaseQuery) ||
            avatar.tags.any { tag -> tag.lowercase().contains(lowercaseQuery) }
        }
    }
    
    fun getSuggestions(userPreferences: List<String> = emptyList()): List<Avatar> {
        // Sugerencias basadas en preferencias del usuario
        val suggestedAvatars = mutableListOf<Avatar>()
        
        // Siempre incluir los populares de cada categorÃ­a
        suggestedAvatars.addAll(getPopular().take(40))
        
        // Agregar avatares relacionados con las preferencias
        userPreferences.forEach { preference ->
            val matchingAvatars = search(preference)
            suggestedAvatars.addAll(matchingAvatars)
        }
        
        // Remover duplicados y limitar
        return suggestedAvatars.distinctBy { it.id }.take(48)
    }
